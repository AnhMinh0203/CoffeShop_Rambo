    private void btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddActionPerformed
        // TODO add your handling code here:
        MenuModel menuModel = new MenuModel();
        MenuController menuController = new MenuController(menuModel);
        Menu menu = new Menu();
        menu.Id = Integer.parseInt(txt_Id.getText());
        menu.NameDrink = txt_Name1.getText();
        menu.Price = Double.parseDouble(txt_Price1.getText());
        menu.NameType = ccb_NameType1.getSelectedItem().toString();
        menu.Status = 0;
        if(cb_StopSales.isSelected()) {
            menu.Status = 1;
        }
        if(menuController.UpdateFood(menu)) {
            JOptionPane.showMessageDialog(this, "Sửa đồ uống thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            JOptionPane.showMessageDialog(this, "Sửa đồ uống thất bại", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_AddActionPerformed

    private void btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UpdateActionPerformed
        // TODO add your handling code here:
        MenuModel menuModel = new MenuModel();
        MenuController menuController = new MenuController(menuModel);
        Menu menu = new Menu();
        menu.Id = Integer.parseInt(txt_Id.getText());
        menu.NameDrink = txt_Name1.getText();
        menu.Price = Double.parseDouble(txt_Price1.getText());
        menu.NameType = ccb_NameType1.getSelectedItem().toString();
        menu.Status = 0;
        if(cb_StopSales.isSelected()) {
            menu.Status = 1;
        }
        if(menuController.UpdateFood(menu)) {
            JOptionPane.showMessageDialog(this, "Sửa đồ uống thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            MenuManager menuFrame = new MenuManager();
            menuFrame.setVisible(true);
            this.setVisible(false);
        }
        else {
            JOptionPane.showMessageDialog(this, "Sửa đồ uống thất bại", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_UpdateActionPerformed

